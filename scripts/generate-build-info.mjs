#!/usr/bin/env node

import { execSync } from "node:child_process"
import { writeFileSync } from "node:fs"
import { resolve } from "node:path"

function runGitCommand(command, fallbackValue) {
  try {
    return execSync(command, { encoding: "utf8" }).trim()
  } catch {
    return fallbackValue
  }
}

const buildInfo = {
  commit: runGitCommand("git rev-parse --short=12 HEAD", "unknown"),
  branch: runGitCommand("git rev-parse --abbrev-ref HEAD", "unknown"),
  dirty: runGitCommand("git status --porcelain", "").length > 0,
  generatedAt: new Date().toISOString()
}

const buildInfoPath = resolve(process.cwd(), "extension/src/build-info.js")
const content =
  "// Auto-generated by scripts/generate-build-info.mjs. Do not edit by hand.\n" +
  `export const BUILD_INFO = ${JSON.stringify(buildInfo, null, 2)}\n`

writeFileSync(buildInfoPath, content, "utf8")
console.log(`Updated ${buildInfoPath}`)
